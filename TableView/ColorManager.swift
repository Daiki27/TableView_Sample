import UIKit
import Foundation

class ColorManager {
    func my_black() -> UIColor {
        return UIColor(red: 50/255, green: 50/255, blue: 50/255, alpha: 1.0)
    }
    func my_white() -> UIColor {
        return UIColor(red: 210/255, green: 210/255, blue: 210/255, alpha: 1.0)
    }
    func my_blue() -> UIColor{
        return UIColor(red: 0/255, green: 154/255, blue: 163/255, alpha: 1.0)
    }
    func my_green() -> UIColor{
        return UIColor(red: 28/255, green: 84/255, blue: 56/255, alpha: 1.0)
    }
}
